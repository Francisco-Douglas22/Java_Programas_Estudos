public class A3_EstruturasAninhadas {
    public static void main(String[] args){
        for (int indice=1; indice <= 10; indice++){
            System.out.println(" ");

            for (int j = 1; j <= 10; j++){
                System.out.println(indice + " X " + j + " = " + (indice * j));
            }
        }
    }
    
}

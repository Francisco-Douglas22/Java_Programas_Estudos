public class A3_Tipos_De_Dados {
    public static void main(String[] args){

        //Tipo String
        String nome = "Francisco Douglas";

        //Tipo Inteiro int
        int idade = 28;

        //Tipo Float ou valor real
        float reais = 123;

        //Tipo real
        double real = 1234.556;

        //Tipo Booleano
        boolean verdade = true;

        System.out.println("O Nome e: "+nome+" A idade e "+idade);
        System.out.println("Tem: "+reais+" ou "+real+" Iso e "+verdade);
    }
}

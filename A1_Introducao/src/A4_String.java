public class A4_String {
    public static void main(String[] args){

        String valor = "Descomplicando o JAVA";

        //Verificando se na string contem a palavra JAVA
        System.out.println(valor.contains("JAVA"));

        //Verificando se na string contem ABC
        System.out.println(valor.contains("ABC"));

        //Utilizando o length para saber o tamanho da string
        System.out.println("Comprimento da Variavel e: "+valor.length());
    }
}

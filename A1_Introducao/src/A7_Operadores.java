public class A7_Operadores {
    public static void main(String[] args){

        int a = 4, b = 5;

        System.out.println("A e maior que B? "+(a > b));
        System.out.println("A e menor que B? "+(a < b));
        System.out.println("A e igual a B? "+(a == b));
        System.out.println("A e maior que B e B e igual a 9? "+(a > b && b == 9));
        System.out.println("A e menor que B e B e igual a 9? "+(a < b && b == 9));
    }
}

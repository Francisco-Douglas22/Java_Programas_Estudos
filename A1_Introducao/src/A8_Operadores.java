public class A8_Operadores {
    public static void main(String[] args){

        int numero = 5;

        numero += 1;
        System.out.println("O numero e "+numero);

        //Uma das formas de inverter uma bool
        boolean valor = true;
        System.out.println(valor);

        //Modificando o valor
        valor = !valor;
        System.out.println(valor);
    }
}

public class Instrucoes_if {
    public static void main(String[] args){
        int valor = 4;

        if(valor == 9){
            System.out.println("Valor = 9: ");
        }else if (valor == 8){
            System.out.println("Valor = 8: ");
        }else{
            System.out.println("O Valor e invalido");
        }
    }
}
